% -----------------------------------------------------------------------------
function problem = rosenbrock()
%ROSENBROCK    Rosenbrock function, cf.
% <https://en.wikipedia.org/wiki/Rosenbrock_function>.

    problem.f = @f;
    problem.gradient = @gradient;
    problem.hessian = @hessian;

end
% -----------------------------------------------------------------------------
function out = f(x)
  out = (1-x(:,1)).^2 + 100*(x(:,2) - x(:,1).^2).^2;
end
% -----------------------------------------------------------------------------
function out = gradient(x)
  out = [-2*(1-x(1)) - 400*(x(2) - x(1)^2) * x(1); ...
         200*(x(2) - x(1)^2) ...
        ];
end
% -----------------------------------------------------------------------------
function out = hessian(x)
  out = [2 + 800*x(1)^2 - 400*(x(2)-x(1)^2), -400*x(1); ...
         -400*x(1),                          200 ...
        ];
end
